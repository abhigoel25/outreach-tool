async def send_portal_message(
    page: Page,
    profile_url: str,
    subject: str,
    body: str,
    dry_run: bool = False,
) -> bool:
    """
    Navigate to a profile page, open the Message dialog, fill and send.
    Returns True on success.
    """
    try:
        await page.goto(profile_url, wait_until="networkidle", timeout=45_000)
        await asyncio.sleep(random.uniform(3.0, 5.0))
    except PwTimeout:
        # networkidle can time out on heavy LWC pages — continue anyway
        await asyncio.sleep(2.0)

    if dry_run:
        return True  # Stop here — don't interact with the page

    # Bail out fast if the portal returned "Invalid Page"
    try:
        body_text = await page.locator("body").inner_text(timeout=3_000)
        if "Invalid Page" in body_text or "Page Not Found" in body_text:
            log.info(f"    [portal] Invalid/not-found page: {profile_url}")
            return False
    except Exception:
        pass

    # --- Step 1: Find and click the Message button ---
    # Dump all visible button texts for debugging on first failure
    clicked = False
    for label in ["Message", "Send Message", "Send a Message", "Direct Message"]:
        try:
            if await _shadow_click(page, label):
                clicked = True
                break
        except Exception:
            pass

    if not clicked:
        # Fallback: Playwright locator (handles light DOM and simple shadow piercing)
        for selector in ["button:has-text('essage')", "[data-label*='essage']", "a:has-text('essage')"]:
            try:
                btn = page.locator(selector).first
                await btn.wait_for(timeout=4_000)
                await btn.click()
                clicked = True
                break
            except Exception:
                pass

    if not clicked:
        # Debug: dump button texts and screenshot so we can see what's on the page
        try:
            btns = await page.evaluate("""() => {
                function collect(root, out=[]) {
                    for (const el of root.querySelectorAll('button,a[role="button"]')) {
                        out.push((el.textContent||'').trim().slice(0,60));
                    }
                    for (const el of root.querySelectorAll('*')) {
                        if (el.shadowRoot) collect(el.shadowRoot, out);
                    }
                    return out;
                }
                return collect(document);
            }""")
            log.info(f"    [portal] Visible buttons: {[b for b in btns if b][:20]}")
        except Exception:
            pass
        try:
            screenshot_path = Path(__file__).parent / "debug_portal.png"
            await page.screenshot(path=str(screenshot_path))
            log.info(f"    [portal] Screenshot saved to {screenshot_path}")
        except Exception:
            pass
        log.info(f"    [portal] Could not find Message button on {profile_url}")
        return False

    await asyncio.sleep(random.uniform(1.5, 2.5))

    # --- Step 2: Fill subject ---
    subject_filled = False
    for sel in ["input[name='subject']", "input[placeholder*='ubject']",
                "input[name='Subject']", "input.subject", "lightning-input input"]:
        if await _shadow_fill(page, sel, subject):
            subject_filled = True
            break
    if not subject_filled:
        # Try pressing Tab to focus subject field if modal just opened
        try:
            await page.keyboard.press("Tab")
            await page.keyboard.type(subject)
            subject_filled = True
        except Exception:
            pass

    await asyncio.sleep(0.5)

    # --- Step 3: Fill body ---
    body_filled = False
    for sel in ["textarea[name='body']", "textarea[name='Body']", "textarea[name='message']",
                "textarea[placeholder*='essage']", "textarea", "div[contenteditable='true']"]:
        if await _shadow_fill(page, sel, body):
            body_filled = True
            break
    if not body_filled:
        log.info(f"    [portal] Could not fill message body on {profile_url}")
        return False

    await asyncio.sleep(0.5)

    # --- Step 4: Click Send ---
    sent = False
    for label in ["Send", "Send Message", "Submit"]:
        try:
            if await _shadow_click(page, label):
                sent = True
                break
        except Exception:
            pass

    if not sent:
        try:
            btn = page.locator("button:has-text('Send')").last
            await btn.click()
            sent = True
        except Exception:
            pass

    if not sent:
        log.info(f"    [portal] Could not click Send on {profile_url}")
        return False

    await asyncio.sleep(random.uniform(1.5, 2.0))
    return True


# ---------------------------------------------------------------------------
# Batch runner
# ---------------------------------------------------------------------------

async def run_batch(limit: int = None, dry_run: bool = False, headless: bool = False):
    from database import (
        init_db, get_pending_columbia_contacts,
        mark_columbia_sent, mark_columbia_failed,
        how_many_columbia_sent_today,
    )
    from email_generator import generate_email

    init_db()

    sent_today = how_many_columbia_sent_today()
    cap = limit if limit else DAILY_LIMIT
    remaining = cap - sent_today

    if remaining <= 0:
        log.info(f"[portal] Columbia daily limit ({cap}) already reached. Come back tomorrow!")
        return

    contacts = get_pending_columbia_contacts(limit=remaining)
    if not contacts:
        log.info("[portal] No Columbia contacts with portal profile URLs pending.")
        return

    log.info(f"{'[DRY RUN] ' if dry_run else ''}[portal] Sending to {len(contacts)} Columbia alumni "
             f"(limit: {cap}/day, sent today: {sent_today})")

    PLAYWRIGHT_PROFILE.mkdir(parents=True, exist_ok=True)

    async with async_playwright() as pw:
        context: BrowserContext = await pw.chromium.launch_persistent_context(
            str(PLAYWRIGHT_PROFILE),
            headless=headless,
            args=["--disable-blink-features=AutomationControlled"],
            viewport={"width": 1280, "height": 900},
        )
        page: Page = await context.new_page()

        # Make sure the session is alive before starting the batch
        if not await _ensure_logged_in(page):
            log.info("[portal] Could not log in — aborting batch.")
            await context.close()
            return

        for i, contact in enumerate(contacts, 1):
            name = f"{contact['first_name']} {contact.get('last_name', '')}"
            co   = contact.get("company", "?")
            path = contact.get("portal_profile_url", "")
            url  = PROFILE_BASE + path

            try:
                subject, body = generate_email(contact)
            except Exception as e:
                log.info(f"[{i}/{len(contacts)}] {name} — email gen failed: {e}")
                mark_columbia_failed(contact["id"], str(e))
                continue

            role = contact.get("role") or "(no role)"
            log.info(f"[{i}/{len(contacts)}] {name} | {role} @ {co}")
            log.info(f"  Subject: {subject}")
            log.info(f"  Message:\n{body}")

            if dry_run:
                log.info("-" * 60)
                continue

            try:
                ok = await send_portal_message(page, url, subject, body, dry_run=dry_run)
            except Exception as e:
                log.info(f"  FAILED: {e}")
                mark_columbia_failed(contact["id"], str(e))
                continue

            if ok:
                mark_columbia_sent(contact["id"])
                log.info(f"  Sent!")
            else:
                mark_columbia_failed(contact["id"])
                log.info(f"  Failed (see above).")

            # Polite delay between messages (3-7s)
            await asyncio.sleep(random.uniform(3.0, 7.0))

        await context.close()

    log.info("[portal] Batch complete.")


if __name__ == "__main__":
    import argparse
    parser = argparse.ArgumentParser(description="Columbia portal messenger")
    parser.add_argument("--limit", type=int, default=None)
    parser.add_argument("--dry-run", action="store_true")
    parser.add_argument("--headless", action="store_true")
    a = parser.parse_args()
    asyncio.run(run_batch(limit=a.limit, dry_run=a.dry_run, headless=a.headless))
